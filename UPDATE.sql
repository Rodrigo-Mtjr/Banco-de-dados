SELECT *
INTO TEMP --CRIANDO UMA TABELA COM O NOME TEMP
FROM ALUNOS; --UTILIZANDO TODAS AS INFORMAÇÕES DA TABELA ALUNOS PARA ISSO

SELECT * 
FROM TEMP; --VERIFICANDO SE DEU CERTO.

UPDATE TEMP	--ATUALIZANDO AS INFORMAÇÕES DA TABELA TEMP
	SET SEXO = 'F' --COLOCANDO COMO 'F' NO CAMPO DE SEXO
	WHERE ID_ALUNO BETWEEN 1 AND 4; --APENAS OS ALUNOS COM OS IDs ENTRE 1 A 4

UPDATE TEMP --ATUALIANDO A TABELA TEMP
	SET NOME = UPPER(NOME); --COLOCANDO OS NOMES DOS ALUNOS EM LETRAS MAISCULAS

SELECT *
FROM TEMP; --VERIFICANDO A TABELA SE DEU CERTO

UPDATE TEMP --ATUALIANDO A TABELA TEMP
	SET NOME = LOWER(NOME), --COLOCANDO TODOS OS NOMES EM MINUSCULA 
		SEXO = UPPER(SEXO) --E OS SEXOS EM MAIUSCULAS 
WHERE ID_ALUNO BETWEEN 1 AND 5; --APENAS DOS ALUNOS COM ID DE 1 A 5

