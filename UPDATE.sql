	SET SEXO = UPPER(SEXO), --COLOCANDO TODOS OS SEXOS EM MAIUSCULAS
		NOME = UPPER(NOME); --COLOCANDO TODOS OS NOMES EM MAIUSCULAS

SELECT *
FROM TEMP;

